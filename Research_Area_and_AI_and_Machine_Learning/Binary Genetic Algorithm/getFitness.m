        3*xy(:,1).^2 -14*xy(:,2) + 6*xy(:,1).*xy(:,2) + 3*xy(:,2).^2) )...
        .* (30 + ((2*xy(:,1) - 3*xy(:,2)).^2).*(18 - 32*xy(:,1) + ...
        12*xy(:,1).^2 + 48*xy(:,2) - 36*xy(:,1).*xy(:,2) + 27*xy(:,2).^2));
